    private void prodComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_prodComboActionPerformed
        if (prodCombo.getSelectedItem().toString().equals("Select a product code:") == false) {
            ArrayList<String> AttOfProd = DBA.getProductAttributes(StartPageFrame.id,
                    Integer.parseInt(prodCombo.getSelectedItem().toString()));
            prName.setText(AttOfProd.get(0));
            prCat.setText(AttOfProd.get(1));
            prPrice.setText(AttOfProd.get(2) + " $");
            prAvQty.setText(AttOfProd.get(3));
        }
    }//GEN-LAST:event_prodComboActionPerformed

    private void proceedToOrderButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_proceedToOrderButActionPerformed
        int orderNum = Integer.parseInt(orderNumber.getText());
        if (AC.checkOrderNum(orderNum)) {
            selectedOrderNum=orderNum;
            orderPanel.setVisible(true);
            Date D = new Date();
            Timestamp TS = new Timestamp(D.getTime());
            orderNumLabel.setText("Order number: "+selectedOrderNum);
            brIdLabel.setText("Branch id: " + StartPageFrame.id);
            cashIdLabel.setText("Cashier id: " + selectedCashId);
            dateTime.setText(TS.toString());
        } else {
            JOptionPane.showMessageDialog(this, "Please select another order number", "order "
                    + "number already exists", JOptionPane.ERROR_MESSAGE);
            orderNumber.setText(null);
        }
    }//GEN-LAST:event_proceedToOrderButActionPerformed

    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        if(Integer.parseInt(quantitySpinner.getValue().toString())>Integer.parseInt(prAvQty.getText())==false){
        cartArea.append(prodCombo.getSelectedItem().toString() + "\t" + prName.getText() + "\t"
                + quantitySpinner.getValue().toString()+"\t"+prPrice.getText()+" /unit\n");
        AC.addToCart(selectedOrderNum, Integer.parseInt(prodCombo.getSelectedItem().toString())
                , Integer.parseInt(quantitySpinner.getValue().toString()));
        prodCombo.setSelectedItem("Select a product code:");
        prName.setText(null);
        prCat.setText(null);
        prPrice.setText(null);
        prAvQty.setText(null);
        quantitySpinner.setValue(1);
        }
        else JOptionPane.showMessageDialog(this, "Please select a lower quantity", "quantity "
                    + "required isn't available", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_addButtonActionPerformed

    private void addOrderButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addOrderButActionPerformed
        if(cashRadBut.isSelected())
            SelPaymentType="Cash";
        else if(creditRadBut.isSelected())
            SelPaymentType="Credit";
        DBA.addOrder(AC.addOrder(selectedOrderNum, selectedCustomerId, selectedCashId, StartPageFrame.id
                , dateTime.getText(), SelPaymentType));
        JOptionPane.showMessageDialog(this, "Order added", " Success", JOptionPane.INFORMATION_MESSAGE);
        for(int i=0;i<AC.getCart().size();i++){
            DBA.addIncludes(AC.getCart().get(i));
            DBA.updateProdQuantity(StartPageFrame.id, AC.getCart().get(i).getProd_code(), 
                    AC.getCart().get(i).getQtyBought());
        }
        ArrayList<Double> totPriceOfEachIncludes=DBA.getOrderTotalPriceOfEachInc(selectedOrderNum);
        for(int i=0;i<totPriceOfEachIncludes.size();i++)
            totalPrice+=totPriceOfEachIncludes.get(i);
        totalToPay.setText(String.valueOf(totalPrice)+" $");
    }//GEN-LAST:event_addOrderButActionPerformed

