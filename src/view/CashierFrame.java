    private void selectButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectButActionPerformed
        String selectedCasId = cashCombo.getSelectedItem().toString();
        if (selectedCasId.equals("Select a cashier id:")) {
            JOptionPane.showMessageDialog(this, "Please select a cashier id", "No selected cashier id",
                    JOptionPane.ERROR_MESSAGE);
        } else {
            cashPanel.setVisible(true);
            addCusPanel.setVisible(false);
            productPanel.setVisible(false);
            selectedCashId = Integer.parseInt(selectedCasId);
            custIdCombo.removeAllItems();
            custIdCombo.addItem("Select a customer id:");
            for (int i = 0; i < DBA.getAllCusIds().size(); i++) {
                custIdCombo.addItem(DBA.getAllCusIds().get(i).toString());
            }
        }
    }//GEN-LAST:event_selectButActionPerformed

    private void submitButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButActionPerformed
        int CId = Integer.parseInt(id.getText());
        String cname = name.getText();
        String clname = lname.getText();
        int cAge = Integer.parseInt(age.getText());
        int CPNum = Integer.parseInt(pnum.getText());
        if (AC.checkCustomerId(CId)) {
            DBA.addCustomer(AC.addCustomer(CId, cAge, CPNum, cname, clname));
            JOptionPane.showMessageDialog(this, "Customer added", "Success",
                    JOptionPane.INFORMATION_MESSAGE);
            id.setText(null);
            name.setText(null);
            lname.setText(null);
            age.setText(null);
            pnum.setText(null);
            custIdCombo.removeAllItems();
            custIdCombo.addItem("Select a customer id:");
            for (int i = 0; i < DBA.getAllCusIds().size(); i++) {
                custIdCombo.addItem(DBA.getAllCusIds().get(i).toString());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select another id", "Customer id Already Exists",
                    JOptionPane.ERROR_MESSAGE);
            id.setText(null);
        }
    }//GEN-LAST:event_submitButActionPerformed

