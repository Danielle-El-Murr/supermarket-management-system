    private void prodSubmitButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_prodSubmitButActionPerformed
        int pCode=Integer.parseInt(code.getText());
        String name=pName.getText();
        String cat=category.getText();
        double pPrice=Double.parseDouble(price.getText());
        String pManf=manf.getText();
        int quantity=Integer.parseInt(qty.getText());
        if(AC.checkProductCode(StartPageFrame.id, pCode)){
            DBA.addProduct(AC.addProduct(pCode, name, cat, pPrice, pManf));
            DBA.addContain(AC.addContain(StartPageFrame.id, pCode, quantity));
            JOptionPane.showMessageDialog(this, "Product added", "Success", 
                    JOptionPane.INFORMATION_MESSAGE);
            code.setText(null);
            pName.setText(null);
            category.setText(null);
            price.setText(null);
            manf.setText(null);
            qty.setText(null);
        }
        else{
            JOptionPane.showMessageDialog(this, "Please select another code", "Product Code Already Exists", 
                    JOptionPane.ERROR_MESSAGE);
            code.setText(null);
        }
    }//GEN-LAST:event_prodSubmitButActionPerformed

