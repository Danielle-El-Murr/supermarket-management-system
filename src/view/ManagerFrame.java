    private void cashSubmitButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cashSubmitButActionPerformed
        int casId=Integer.parseInt(id.getText());
        String name=cName.getText();
        String lname=cLName.getText();
        int num=Integer.parseInt(PNum.getText());
        double cSalary=Double.parseDouble(salary.getText());
        int Year=Integer.parseInt(joinYear.getText());
        if(AC.checkCashierId(StartPageFrame.id, casId)){
            DBA.addCashier(AC.addCashier(casId, num, Year, StartPageFrame.id, cSalary, name, lname));
            JOptionPane.showMessageDialog(this, "Cashier added", "Success", 
                    JOptionPane.INFORMATION_MESSAGE);
            id.setText(null);
            cName.setText(null);
            cLName.setText(null);
            PNum.setText(null);
            salary.setText(null);
            joinYear.setText(null);
        }
        else{
            JOptionPane.showMessageDialog(this, "Please select another id", "Cashier Id Already Exists", 
                    JOptionPane.ERROR_MESSAGE);
            id.setText(null);
        }
    }//GEN-LAST:event_cashSubmitButActionPerformed

    private void updateButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButActionPerformed
        int casId=Integer.parseInt(id.getText());
        int num=Integer.parseInt(PNum.getText());
        double cSalary=Double.parseDouble(salary.getText());
        if(AC.checkCashierId(StartPageFrame.id, casId)==false){
            DBA.updateCashier(casId, cSalary, num);
            JOptionPane.showMessageDialog(this, "Cashier updated", "Success", 
                    JOptionPane.INFORMATION_MESSAGE);
            id.setText(null);
            cName.setText(null);
            cLName.setText(null);
            PNum.setText(null);
            salary.setText(null);
            joinYear.setText(null);
        }
        else{
            JOptionPane.showMessageDialog(this, "Please select an existing cashier id", "Cashier Id Doesn't Exist", 
                    JOptionPane.ERROR_MESSAGE);
            id.setText(null);
        }
    }//GEN-LAST:event_updateButActionPerformed

